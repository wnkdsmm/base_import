def _iter_python_files(base: Path) -> list[Path]:
    return [
        path
        for path in base.rglob("*.py")
        if "__pycache__" not in path.parts and ".venv" not in path.parts and ".git" not in path.parts
    ]


def _count_routes(file_paths: list[Path]) -> tuple[int, list[str]]:
    routes: list[str] = []
    for path in file_paths:
        tree = ast.parse(path.read_text(encoding="utf-8"))
        for node in ast.walk(tree):
            if not isinstance(node, ast.Call):
                continue
            func = node.func
            if not isinstance(func, ast.Attribute):
                continue
            if not isinstance(func.value, ast.Name) or func.value.id != "router":
                continue
            if func.attr not in {"get", "post", "put", "patch", "delete"}:
                continue
            if node.args and isinstance(node.args[0], ast.Constant) and isinstance(node.args[0].value, str):
                routes.append(node.args[0].value)
    return len(routes), sorted(routes)


def collect_project_facts() -> dict[str, object]:
    python_files = _iter_python_files(ROOT)
    app_service_count = len(_iter_python_files(ROOT / "app" / "services"))
    dashboard_count = len(_iter_python_files(ROOT / "app" / "dashboard"))
    processing_count = len(_iter_python_files(ROOT / "core" / "processing"))
    mapping_count = len(_iter_python_files(ROOT / "core" / "mapping"))

    route_dir = ROOT / "app" / "routes"
    api_files = [path for path in route_dir.glob("api*.py") if path.is_file()]
    page_files = [route_dir / "pages.py"]
    api_endpoint_count, api_routes = _count_routes(api_files)
    page_route_count, page_routes = _count_routes(page_files)

    test_files = [
        path
        for path in (ROOT / "tests").rglob("*.py")
        if "__pycache__" not in path.parts and (path.name.startswith("test") or path.name.endswith("_cases.py"))
    ]

    requirements = [
        line.strip()
        for line in (ROOT / "requirements.txt").read_text(encoding="utf-8").splitlines()
        if line.strip() and not line.strip().startswith("#")
    ]

    return {
        "python_files": len(python_files),
        "service_files": app_service_count,
        "dashboard_files": dashboard_count,
        "processing_files": processing_count,
        "mapping_files": mapping_count,
        "test_files": len(test_files),
        "api_endpoint_count": api_endpoint_count,
        "page_route_count": page_route_count,
        "api_routes": api_routes,
        "page_routes": page_routes,
        "requirements": requirements,
    }


def set_document_defaults(document: Document) -> None:
    section = document.sections[0]
    section.top_margin = Cm(2)
    section.bottom_margin = Cm(2)
    section.left_margin = Cm(2.5)
    section.right_margin = Cm(2)

    styles = document.styles
    styles["Normal"].font.name = "Times New Roman"
    styles["Normal"]._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")
    styles["Normal"].font.size = Pt(12)

    for style_name in ("Title", "Heading 1", "Heading 2", "Heading 3"):
        style = styles[style_name]
        style.font.name = "Times New Roman"
        style._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")


def add_title(document: Document, text: str, subtitle: str | None = None) -> None:
    paragraph = document.add_paragraph()
    paragraph.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = paragraph.add_run(text)
    run.bold = True
    run.font.name = "Times New Roman"
    run._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")
    run.font.size = Pt(16)

    if subtitle:
        paragraph = document.add_paragraph()
        paragraph.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = paragraph.add_run(subtitle)
        run.italic = True
        run.font.name = "Times New Roman"
        run._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")
        run.font.size = Pt(12)


